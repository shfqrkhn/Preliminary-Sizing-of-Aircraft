    public double gimmeWeW0R(double givenW0Guess){
        double numW0GuessR, numAR, numCR, numKvsR, numMR, numWeW0R;
        numW0GuessR = givenW0Guess;
        numAR = Double.parseDouble(txtAR.getText());
        numCR = Double.parseDouble(txtCR.getText());
        numKvsR = Double.parseDouble(txtKvsR.getText());
        numMR = Double.parseDouble(txtMR.getText());
        numWeW0R = numAR * (Math.pow(numW0GuessR, -numCR)) * numKvsR * numMR;
        return numWeW0R;
    }
    
    public double gimmeW0CalcR(double givenW0Guess){
        double numWPLR, numTotalCrewWeightR, numWfW0R, numWeW0R, numResultR;
        numWPLR = Double.parseDouble(txtWPLR.getText());
        numTotalCrewWeightR = Double.parseDouble(txtTotalCrewWeightR.getText());
        numWfW0R = Double.parseDouble(txtWfW0R.getText());
        numWeW0R = gimmeWeW0R(givenW0Guess);
        numResultR = (numWPLR + numTotalCrewWeightR)/(1 - numWfW0R - numWeW0R);
        return numResultR;
    }
    
    public void gimmeConvergenceR(double givenW0Guess){
        double W0GuessR, tolerance, numPercentToleranceR, numNumberOfIterationsR, numW0CalcR;
        numPercentToleranceR = Double.parseDouble(txtPercentToleranceR.getText());
        numNumberOfIterationsR = 0.0;
        W0GuessR = givenW0Guess;
        boolean loop = true;
        while(loop){
            numNumberOfIterationsR++;
            numW0CalcR = gimmeW0CalcR(W0GuessR);
            tolerance = (Math.abs((W0GuessR - numW0CalcR)/numW0CalcR)) * 100;
            if(tolerance>numPercentToleranceR){
                //W0GuessR++;
                if(tolerance>numPercentToleranceR){
                    W0GuessR = numW0CalcR;
                }
            }
            else{
                loop = false;
                txtNumberOfIterationsR.setText(String.valueOf(numNumberOfIterationsR));
                txtResultR.setText(String.valueOf(W0GuessR));
            }
        }
    }
    
    public double gimmeWFUsed(double givenWTOGuess){
        double numMff,numWTOGuess,numWFused;
        numMff = Double.parseDouble(txtMff.getText());
        numWTOGuess = givenWTOGuess;
        numWFused = (1-numMff)*numWTOGuess;
        return numWFused;
    }
    
    public double gimmeWF(double givenWTOGuess){
        double numMff, numWTOGuess, numWFres, numWF, numWFused;
        numMff = Double.parseDouble(txtMff.getText());
        numWTOGuess = givenWTOGuess;
        numWFres = Double.parseDouble(txtWFres.getText());
        numWFused = Double.parseDouble(txtWFused.getText());
        numWF = ((1-numMff)*numWTOGuess)+(numWFres*numWFused);
        return numWF;
    }
    
    public double gimmeWOEtent(double givenWTOGuess){
        double numWTOGuess, numWF, numWPL, numWOEtent;
        numWTOGuess = givenWTOGuess;
        numWF = gimmeWF(givenWTOGuess);
        numWPL = Double.parseDouble(txtWPL.getText());
        numWOEtent = numWTOGuess - numWF - numWPL;
        return numWOEtent;
    }
    
    public double gimmeWEtent(double givenWTOGuess){
        double numWOEtent, numWtfo, numTotalCrewWeight, numWEtent;
        numWOEtent = gimmeWOEtent(givenWTOGuess);
        numWtfo = Double.parseDouble(txtWtfo.getText());
        numTotalCrewWeight = Double.parseDouble(txtTotalCrewWeight.getText());
        numWEtent = numWOEtent - numWtfo - numTotalCrewWeight;
        return numWEtent;
    }
    
    public double gimmeWE(double givenWTOGuess){
        double numWTOGuess, numWEAProp, numWEBProp, numWEAJet, numWEBJet, numWE, x;
        numWTOGuess = givenWTOGuess;
        numWEAProp = Double.parseDouble(txtWEAProp.getText());
        numWEBProp = Double.parseDouble(txtWEBProp.getText());
        x = ((Math.log10(numWTOGuess))-numWEAProp)/numWEBProp;
        numWE = Math.pow(10.0, x);
        return numWE;
    }
    
    public double gimmeResult(double givenWTOGuess){
        double numWE, numWF, numWPL, numTotalCrewWeight, numResult;
        numWE = gimmeWE(givenWTOGuess);
        numWF = gimmeWF(givenWTOGuess);
        numWPL = Double.parseDouble(txtWPL.getText());
        numTotalCrewWeight = Double.parseDouble(txtTotalCrewWeight.getText());
        numResult = numWE + numWF + numWPL + numTotalCrewWeight;
        return numResult;
    }
    
    public void gimmeConvergence(double givenWTOGuess){
        double guess, we, weTent, tolerance, numPercentTolerance, result, numNumberOfIterations;
        numPercentTolerance = Double.parseDouble(txtPercentTolerance.getText());
        numNumberOfIterations = 0.0;
        boolean loop = true;
        guess = givenWTOGuess;
        while(loop){
            numNumberOfIterations++;
            we = gimmeWE(guess);
            weTent = gimmeWEtent(guess);
            result = gimmeResult(guess);
            tolerance = (Math.abs((weTent-we)/we))*100;
            if(tolerance>numPercentTolerance){
                guess = result;
            }
            else{
                loop = false;
                txtNumberOfIterations.setText(String.valueOf(numNumberOfIterations));
                txtResult.setText(String.valueOf(guess));
            }
        }
    }
    
